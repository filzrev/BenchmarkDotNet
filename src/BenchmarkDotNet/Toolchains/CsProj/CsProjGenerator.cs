        protected override string GetBuildArtifactsDirectoryPath(BuildPartition buildPartition, string programName)
        {
            string assemblyLocation = buildPartition.RepresentativeBenchmarkCase.Descriptor.Type.Assembly.Location;

            //Assembles loaded from a stream will have an empty location (https://docs.microsoft.com/en-us/dotnet/api/system.reflection.assembly.location).
            string directoryName = assemblyLocation.IsEmpty() ?
                Path.Combine(Directory.GetCurrentDirectory(), "BenchmarkDotNet.Bin") :
                Path.GetDirectoryName(buildPartition.AssemblyLocation)!;

            return Path.Combine(directoryName, programName);
        }

        protected override string GetProjectFilePath(string buildArtifactsDirectoryPath)
            => Path.Combine(buildArtifactsDirectoryPath, AutogeneratedProjectName);

        protected override string GetBinariesDirectoryPath(string buildArtifactsDirectoryPath, string configuration)
            => Path.Combine(buildArtifactsDirectoryPath, "bin", configuration, TargetFrameworkMoniker);

        protected override async ValueTask GenerateCustomBuildHooksAsync(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            var benchmark = buildPartition.RepresentativeBenchmarkCase;
            var benchmarkProjectFile = GetProjectFilePath(benchmark.Descriptor.Type, logger);

            var buildArtifactsDirectoryPath = artifactsPaths.BuildArtifactsDirectoryPath;

            // Trim tfm part from binariesDirectoryPath on IntegrationTests. (When ArtifactsPath is not used, it's appended automatically)
            var binariesDirectoryPath = buildPartition.ForcedNoDependenciesForIntegrationTests
                ? artifactsPaths.BinariesDirectoryPath.Substring(0, artifactsPaths.BinariesDirectoryPath.Length - TargetFrameworkMoniker.Length - 1)
                : artifactsPaths.BinariesDirectoryPath;

            var propsPath = Path.Combine(buildArtifactsDirectoryPath, "BenchmarkDotNet.Build.props");
            var props = GetTemplateContent("BenchmarkDotNet.Build.props.txt");
            await File.WriteAllTextAsync(propsPath, props.ToString(), cancellationToken).ConfigureAwait(false);

            var targetsPath = Path.Combine(buildArtifactsDirectoryPath, "BenchmarkDotNet.Build.targets");
            var targets = GetTemplateContent("BenchmarkDotNet.Build.targets.txt");
            await File.WriteAllTextAsync(targetsPath, targets.ToString(), cancellationToken).ConfigureAwait(false);

            string GetTemplateContent(string templateName)
                => new StringBuilder(ResourceHelper.LoadTemplate(templateName))
                    .Replace("$BENCHMARK_PROJ$", benchmarkProjectFile.Name)
                    .Replace("$AUTOGENERATED_PROJ$", AutogeneratedProjectName)
                    .Replace("$OUTPUT_PATH$", binariesDirectoryPath)
                    .Replace("$PUBLISH_DIR$", artifactsPaths.PublishDirectoryPath)
                    .ToString();
        }

        protected override ValueTask GenerateBuildScriptAsync(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, CancellationToken cancellationToken)
        {
            string projectFilePath = GetProjectFilePath(buildPartition.RepresentativeBenchmarkCase.Descriptor.Type, NullLogger.Instance).FullName;

            var content = new StringBuilder(300)
                .AppendLine($"call {CliPath} {DotNetCliCommand.GetRestoreCommand(artifactsPaths, buildPartition, projectFilePath)}")
                .AppendLine($"call {CliPath} {DotNetCliCommand.GetBuildCommand(artifactsPaths, buildPartition, projectFilePath, TargetFrameworkMoniker)}")
                .AppendLine($"call {CliPath} {DotNetCliCommand.GetRestoreCommand(artifactsPaths, buildPartition, artifactsPaths.ProjectFilePath)}")
                .AppendLine($"call {CliPath} {DotNetCliCommand.GetBuildCommand(artifactsPaths, buildPartition, artifactsPaths.ProjectFilePath, TargetFrameworkMoniker)}")
                .ToString();

            return new(File.WriteAllTextAsync(artifactsPaths.BuildScriptFilePath, content, cancellationToken));
        }

        protected override async ValueTask GenerateProjectAsync(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            // Generate Autogenerated project file.
            await File.WriteAllTextAsync(
                artifactsPaths.ProjectFilePath,
                await GenerateBuildProject(buildPartition, artifactsPaths, logger, cancellationToken).ConfigureAwait(false),
                cancellationToken
            )
                .ConfigureAwait(false);

            // Integration tests are built without dependencies, so we skip gathering dlls.
            if (buildPartition.ForcedNoDependenciesForIntegrationTests)
                return;

            // Build original benchmark project.
            await BuildOriginalBenchmarkProjectAsync(buildPartition, artifactsPaths, logger, cancellationToken).ConfigureAwait(false);

            // Gather references and update Autogenerated project file.
            await GatherReferencesAsync(buildPartition, artifactsPaths, logger, cancellationToken).ConfigureAwait(false);
        }

        private async ValueTask<string> GenerateBuildProject(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            var benchmark = buildPartition.RepresentativeBenchmarkCase;
            var projectFile = GetProjectFilePath(benchmark.Descriptor.Type, logger);

            var xmlDoc = new XmlDocument();
            xmlDoc.Load(projectFile.FullName);
            var (customProperties, sdkName) = GetSettingsThatNeedToBeCopied(xmlDoc, projectFile);

            return new StringBuilder(await ResourceHelper.LoadTemplateAsync("CsProj.txt", cancellationToken).ConfigureAwait(false))
                .Replace("$PLATFORM$", buildPartition.Platform.ToConfig())
                .Replace("$CODEFILENAME$", Path.GetFileName(artifactsPaths.ProgramCodePath))
                .Replace("$CSPROJPATH$", projectFile.FullName)
                .Replace("$TFM$", TargetFrameworkMoniker)
                .Replace("$PROGRAMNAME$", artifactsPaths.ProgramName)
                .Replace("$RUNTIMESETTINGS$", GetRuntimeSettings(benchmark.Job.Environment.Gc, buildPartition.Resolver))
                .Replace("$COPIEDSETTINGS$", customProperties)
                .Replace("$SDKNAME$", sdkName)
                .ToString();
        }

        protected async Task BuildOriginalBenchmarkProjectAsync(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            // Gets original benchmark project.
            var projectFilePath = GetProjectFilePath(buildPartition.RepresentativeBenchmarkCase.Descriptor.Type, NullLogger.Instance).FullName;

            // Build the original project.
            var command = new DotNetCliCommand(
                CliPath,
                projectFilePath,
                TargetFrameworkMoniker,
                arguments: "",
                GenerateResult.Success(artifactsPaths, []),
                logger,
                buildPartition,
                [],
                buildPartition.Timeout
            );

            BuildResult buildResult = await command.RestoreThenBuildAsync(cancellationToken).ConfigureAwait(false);

            if (!buildResult.IsBuildSuccess)
            {
                if (!buildResult.TryToExplainFailureReason(out string? reason))
                {
                    reason = buildResult.ErrorMessage;
                }

                // This exception is handeled by caller (GeneratorBase) and converted to GenerateResult.Failure
                throw new InvalidOperationException($"Failed to build original benchmark projects, reason:{Environment.NewLine}{reason}");
            }
        }

        protected async ValueTask GatherReferencesAsync(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            var gatheredData = await GatherReferenceCopyLocalPaths(buildPartition, artifactsPaths, logger, cancellationToken).ConfigureAwait(false);
            var referenceCopyLocalPaths = gatheredData.Items;

            var doc = XDocument.Load(artifactsPaths.ProjectFilePath);

            // Add ItemGroup before custom targets import.
            var itemGroup = new XElement("ItemGroup");
            doc.Root!.LastNode!.AddBeforeSelf(itemGroup);

            // Remove ProjectReference node of benchmark project after gathered dll references are added.
            doc.Root.Descendants("ProjectReference").Single().Remove();

            // Add original benchmark output assembly reference.
            itemGroup.Add(new XElement("Reference",
                new XAttribute("Include", gatheredData.TargetName),
                new XElement("HintPath", gatheredData.TargetPath)
            ));

            // Add transiently referenced project dll references.
            foreach (var item in gatheredData.Items.GetProjectReferences())
            {
                itemGroup.Add(new XElement("Reference",
                    new XAttribute("Include", item.Filename),
                    new XElement("HintPath", item.FullPath)
                // TODO: Add Aliases here for extern alias #2289
                ));
            }

            // Add runtime assets dll references.
            foreach (var item in gatheredData.Items.GetRuntimeAssets())
            {
                itemGroup.Add(new XElement("Reference",
                    new XAttribute("Include", item.Filename),
                    new XElement("HintPath", item.FullPath)
                // TODO: Add Aliases here for extern alias #2289
                ));
            }

            // Native assets are not copied to bin directory when ProjectReference is remove.
            // So it need to add items as <None> element.
            foreach (var item in gatheredData.Items.GetNativeAssets())
            {
                itemGroup.Add(new XElement("None",
                    new XAttribute("Include", item.FullPath),
                    new XElement("TargetPath", item.DestinationSubPath),
                    new XElement("CopyToOutputDirectory", "PreserveNewest") // `IfDifferent` option requires .NET 9 SDK
                ));
            }

            // TODO: Satellite assembly DLLs seems automatically copied to bin directory.
            //foreach (var item in gatheredData.Items.GetResourceAssets())
            //{
            //    itemGroup.Add(new XElement("Reference",
            //        new XAttribute("Include", item.Filename),
            //        new XElement("HintPath", item.FullPath)
            //    ));
            //}

            // Temporary workaround for `System.ValueTuple` related issue.
            // https://github.com/dotnet/maintenance-packages/issues/258
            // Add direct package reference to supress wrong assembly redirect when targetting net471 or later

            // This workaround can be tested with following steps.
            // 1. Disable `ForcedNoDependenciesForIntegrationTests`
            // 2. Run `JitRuntimeValidationTest.CheckClrOnWindows` tests.
            if (TargetFrameworkMoniker.StartsWith("net471") ||
                TargetFrameworkMoniker.StartsWith("net472") ||
                TargetFrameworkMoniker.StartsWith("net48") ||
                TargetFrameworkMoniker.StartsWith("net481"))
            {
                // Add `<PackageReference Include="System.ValueTuple" Version="4.6.2" />`
                itemGroup.Add(new XElement("PackageReference",
                  new XAttribute("Include", "System.ValueTuple"),
                  new XAttribute("Version", "4.6.2")
                ));
            }

            doc.Save(artifactsPaths.ProjectFilePath, SaveOptions.None);
        }

        private async Task<GatheredData> GatherReferenceCopyLocalPaths(BuildPartition buildPartition, ArtifactsPaths artifactsPaths, ILogger logger, CancellationToken cancellationToken)
        {
            // Gets original benchmark project.
            var projectFilePath = GetProjectFilePath(buildPartition.RepresentativeBenchmarkCase.Descriptor.Type, NullLogger.Instance).FullName;

            var command = new DotNetCliCommand(
                CliPath,
                projectFilePath,
                TargetFrameworkMoniker,
                arguments: "",
                GenerateResult.Success(artifactsPaths, []),
                logger,
                buildPartition,
                [],
                buildPartition.Timeout
            );

            // Run `dotnet msbuild` command and gather information.
            var extraArguments = new StringBuilder(128)
                .AppendArgument($"-target:{CustomMsBuildTargetName}")
                .AppendArgument($"-getTargetResult:{CustomMsBuildTargetName}")
                .AppendArgument($"-getProperty:TargetName")
                .AppendArgument($"-getProperty:TargetPath")
                .AppendArgument($"-getResultOutputFile:{CustomMsBuildOutputFileName}")
                .ToString();

            DotNetCliCommandResult msBuildResult = await command.MSBuildNoRestoreAsync(extraArguments, cancellationToken).ConfigureAwait(false);
            if (!msBuildResult.IsSuccess)
            {
                // This exception is handeled by caller (GeneratorBase) and converted to GenerateResult.Failure
                throw new InvalidOperationException($"Failed to gather references from original benchmark projects, reason:{Environment.NewLine}{msBuildResult.StandardError}");
            }

            var outputJsonPath = Path.Combine(artifactsPaths.BuildArtifactsDirectoryPath, CustomMsBuildOutputFileName);
            if (!File.Exists(outputJsonPath))
            {
                throw new InvalidOperationException("Failed to gather references from original benchmark projects");
            }

            var json = File.ReadAllText(outputJsonPath);
            using JsonDocument document = JsonDocument.Parse(json);

            var properties = document.RootElement.GetProperty("Properties");
            string targetName = properties.GetProperty("TargetName").GetString()!;
            string targetPath = properties.GetProperty("TargetPath").GetString()!;

            if (document.RootElement.TryGetProperty("TargetResults", out var targetResults) &&
                targetResults.TryGetProperty(CustomMsBuildTargetName, out var target) &&
                target.TryGetProperty("Result", out var resultElement) &&
                target.TryGetProperty("Items", out var itemsElement)
                && resultElement.GetString() == "Success")
            {

                var referenceCopyLocalPaths = JsonSerializer.Deserialize<ReferenceCopyLocalPaths>(itemsElement, ReferenceCopyLocalPaths.SerializerOptions)!;

                return new GatheredData
                {
                    TargetFramework = TargetFrameworkMoniker,
                    TargetName = targetName,
                    TargetPath = targetPath,
                    Items = referenceCopyLocalPaths,
                };
            }

            throw new InvalidOperationException($"Failed to parse gathered json file. {outputJsonPath}");
        }

        /// <summary>
        /// returns an MSBuild string that defines Runtime settings
        /// </summary>
        [PublicAPI]
        protected virtual string GetRuntimeSettings(GcMode gcMode, IResolver resolver)
        {
            var builder = new StringBuilder(80)
                .AppendLine($"<PropertyGroup>")
                .AppendLine($"    <ServerGarbageCollection>{gcMode.ResolveValue(GcMode.ServerCharacteristic, resolver).ToLowerCase()}</ServerGarbageCollection>")
                .AppendLine($"    <ConcurrentGarbageCollection>{gcMode.ResolveValue(GcMode.ConcurrentCharacteristic, resolver).ToLowerCase()}</ConcurrentGarbageCollection>");

            if (gcMode.HasValue(GcMode.RetainVmCharacteristic))
                builder.AppendLine($"    <RetainVMGarbageCollection>{gcMode.ResolveValue(GcMode.RetainVmCharacteristic, resolver).ToLowerCase()}</RetainVMGarbageCollection>");

            return builder.AppendLine("  </PropertyGroup>").ToString();
        }

        // the host project or one of the .props file that it imports might contain some custom settings that needs to be copied, sth like
        // <NetCoreAppImplicitPackageVersion>2.0.0-beta-001607-00</NetCoreAppImplicitPackageVersion>
        // <RuntimeFrameworkVersion>2.0.0-beta-001607-00</RuntimeFrameworkVersion>
        internal (string customProperties, string sdkName) GetSettingsThatNeedToBeCopied(XmlDocument xmlDoc, FileInfo projectFile)
        {
            if (RuntimeFrameworkVersion.IsNotBlank()) // some power users knows what to configure, just do it and copy nothing more
            {
                return (@$"<PropertyGroup>
    <RuntimeFrameworkVersion>{RuntimeFrameworkVersion}</RuntimeFrameworkVersion>
  </PropertyGroup>", DefaultSdkName);
            }

            XmlElement projectElement = xmlDoc.DocumentElement!;
            // custom SDKs are not added for non-netcoreapp apps (like net471), so when the TFM != netcoreapp we dont parse "<Import Sdk="
            // we don't allow for that mostly to prevent from edge cases like the following
            // <Import Sdk="Microsoft.NET.Sdk.WindowsDesktop" Project="Sdk.props" Condition="'$(TargetFramework)'=='netcoreapp3.0'"/>
            string? sdkName = null;
            if (TargetFrameworkMoniker.StartsWith("netcoreapp", StringComparison.InvariantCultureIgnoreCase))
            {
                foreach (XmlElement importElement in projectElement.GetElementsByTagName("Import"))
                {
                    sdkName = importElement.GetAttribute("Sdk");
                    if (sdkName.IsNotBlank())
                    {
                        break;
                    }
                }
            }
            if (sdkName.IsBlank())
            {
                sdkName = projectElement.GetAttribute("Sdk");
            }
            // If Sdk isn't an attribute on the Project element, it could be a child element.
            if (sdkName.IsBlank())
            {
                foreach (XmlElement sdkElement in projectElement.GetElementsByTagName("Sdk"))
                {
                    sdkName = sdkElement.GetAttribute("Name");
                    if (sdkName.IsBlank())
                    {
                        continue;
                    }
                    string version = sdkElement.GetAttribute("Version");
                    // Version is optional
                    if (version.IsNotBlank())
                    {
                        sdkName += $"/{version}";
                    }
                    break;
                }
            }
            if (sdkName.IsBlank())
            {
                sdkName = DefaultSdkName;
            }

            XmlDocument? itemGroupsettings = null;
            XmlDocument? propertyGroupSettings = null;

            GetSettingsThatNeedToBeCopied(projectElement, ref itemGroupsettings, ref propertyGroupSettings, projectFile);

            List<string> customSettings = new List<string>(2);
            if (itemGroupsettings != null)
            {
                customSettings.Add(GetIndentedXmlString(itemGroupsettings));
            }
            if (propertyGroupSettings != null)
            {
                customSettings.Add(GetIndentedXmlString(propertyGroupSettings));
            }

            return (string.Join(Environment.NewLine + Environment.NewLine, customSettings), sdkName);
        }

        private static void GetSettingsThatNeedToBeCopied(XmlElement projectElement, ref XmlDocument? itemGroupsettings, ref XmlDocument? propertyGroupSettings, FileInfo projectFile)
        {
            CopyProperties(projectElement, ref itemGroupsettings, "ItemGroup");
            CopyProperties(projectElement, ref propertyGroupSettings, "PropertyGroup");

            foreach (XmlElement importElement in projectElement.GetElementsByTagName("Import"))
            {
                string propsFilePath = importElement.GetAttribute("Project");
                var directoryName = projectFile.DirectoryName ?? throw new DirectoryNotFoundException(projectFile.DirectoryName);
                string absolutePath = File.Exists(propsFilePath)
                    ? propsFilePath // absolute path or relative to current dir
                    : Path.Combine(directoryName, propsFilePath); // relative to csproj
                if (File.Exists(absolutePath))
                {
                    var importXmlDoc = new XmlDocument();
                    importXmlDoc.Load(absolutePath);
                    GetSettingsThatNeedToBeCopied(importXmlDoc.DocumentElement!, ref itemGroupsettings, ref propertyGroupSettings, projectFile);
                }
            }
        }

        private static void CopyProperties(XmlElement projectElement, ref XmlDocument? copyToDocument, string groupName)
        {
            XmlElement? itemGroupElement = copyToDocument?.DocumentElement;
            foreach (XmlElement groupElement in projectElement.GetElementsByTagName(groupName))
            {
                foreach (var node in groupElement.ChildNodes)
                {
                    if (node is XmlElement setting && SettingsWeWantToCopy.Contains(setting.Name))
                    {
                        if (copyToDocument is null)
                        {
                            copyToDocument = new XmlDocument();
                            itemGroupElement = copyToDocument.CreateElement(groupName);
                            copyToDocument.AppendChild(itemGroupElement);
                        }
                        XmlNode copiedNode = copyToDocument.ImportNode(setting, true);
                        itemGroupElement!.AppendChild(copiedNode);
                    }
                }
            }
        }

        private static string GetIndentedXmlString(XmlDocument doc)
        {
            StringBuilder sb = new StringBuilder();
            XmlWriterSettings settings = new XmlWriterSettings
            {
                OmitXmlDeclaration = true,
                Indent = true,
                IndentChars = "  "
            };
            using (XmlWriter writer = XmlWriter.Create(sb, settings))
            {
                doc.Save(writer);
            }
            return sb.ToString();
        }

        /// <summary>
        /// returns a path to the project file which defines the benchmarks
        /// </summary>
        [PublicAPI]
        protected virtual FileInfo GetProjectFilePath(Type benchmarkTarget, ILogger logger)
        {
            if (!GetSolutionRootDirectory(out var rootDirectory) && !GetProjectRootDirectory(out rootDirectory))
            {
                logger.WriteLineError(
                    $"Unable to find .sln .slnx or .csproj file. Will use current directory {Directory.GetCurrentDirectory()} to search for project file. If you don't use .sln file on purpose it should not be a problem.");
                rootDirectory = new DirectoryInfo(Directory.GetCurrentDirectory());
            }

            // important assumption! project's file name === output dll name
            string projectName = benchmarkTarget.GetTypeInfo().Assembly.GetName().Name!;

            var projectFile = Helpers.FindProjectFile(rootDirectory, projectName);
            return projectFile;
        }

        public override bool Equals(object? obj) => obj is CsProjGenerator other && Equals(other);

        public bool Equals(CsProjGenerator? other)
        {
            if (ReferenceEquals(this, other))
                return true;

            if (other is null)
                return false;

            return TargetFrameworkMoniker == other.TargetFrameworkMoniker
                && RuntimeFrameworkVersion == other.RuntimeFrameworkVersion
                && CliPath == other.CliPath
                && PackagesPath == other.PackagesPath;
        }

        public override int GetHashCode()
            => HashCode.Combine(TargetFrameworkMoniker, RuntimeFrameworkVersion, CliPath, PackagesPath);

